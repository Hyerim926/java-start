package variable;

public class Var7 {
    public static void main(String[] args) {
        // 개발자가 직접 입력하는 고정된 값 = 리터럴(literal)
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언
        char d = 'A'; // 문자 - 작은 따옴표 (실무 사용 x)
        String e = "Hello Java"; // 문자열 - 큰 따옴표

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
